class PlusPlus{

public static void main(String[] args){

int count = 0;
System.out.println(count++);   // Prints 0. print count value and increase the value by 1
System.out.println(count++);

int i = 0;
System.out.println(++i); // Prints 1. increase the value by 1 and print
System.out.println(++i);

}

}